package day9_tasks;
/*
Create a method that concatenates a String with an Integer.
Overload it to Concatenate a String with  a Double.
Overload it to Concatenate a String with  two Integers.
 */

public class OverloadMethod {

    public String concatenate (String str, Integer num){

        return str + num;

    }
    public String concatenate (String str, Double num){
        return  str + num;


    }
    public String concatenate (String str, Integer num, Integer num1){

        return str + num + num1;


    }








    }
