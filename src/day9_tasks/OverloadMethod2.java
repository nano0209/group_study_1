/*
Declare a method sun() that accepts two int
overload it to accept to double
overload it to accept 3 int
 */

public class OverloadMethod2 {

    public Integer sun(int num, int num1){
        return num + num1;

    }
    public double sun(double num, double num1){
        return num + num1;


    }
    public int sun(int num, int num1, int num2){
        return num + num1 + num2;



    }



    }
